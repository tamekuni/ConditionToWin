package jp.gr.java_conf.tamekuni.condition_to_win;

class TableInfo {

	int mKyotaku;
	int mTsumibo;

	TableInfo() {
		mKyotaku = 0;
		mTsumibo = 0;
	}

	int getKyotaku() {
		return mKyotaku;
	}

	void setKyotaku(int aKyotaku) {
		mKyotaku = aKyotaku;
	}

	int getTsumibo() {
		return mTsumibo;
	}

	void setTsumibo(int aTsumibo) {
		mTsumibo = aTsumibo;
	}
}
